        <p>
            Klimaatverandering vereist oplossingen op (inter)nationaal, lokaal en persoonlijk niveau … Maar om echt een impact te hebben, is het wereldwijd belangrijk om het economische en sociale model te veranderen. Om dit te bereiken, moeten we samenwerken buiten onze eigen cirkel, beyond borders.
        </p>
        <p>
            <a href="https://www.laconditionpublique.com/">La Condition Publique <small>(Roubaix)</small></a>, Festival <a href="https://www.esperanzah.be/">Esperanzah! <small>(Floreffe)</small></a> en het Irie Vibes Roots Festival <small>(Kortemark)</small>, alle drie actief in de sociaal-culturele sector, bundelen hun krachten om niet alleen de eigen ecologische voetafdruk te verkleinen, maar het vuur van de verandering ook aan te wakkeren bij partners en bezoekers. 
            <small>#OnFaitCeQueOnPeut</small>
        </p>
        <p>
            Dit project wordt gesteund door <a href="https://www.interreg-fwvl.eu/nl">Interreg France-Wallonie-Vlaanderen</a> en focust zich op ecologie, onderwijs en
            taal. <small>#Trimthefootprint</small>
        </p>
        <p>
            Naast Trim The Footprint in België hebben wij ook een project in <b>Tanzania,</b> 
            <a href="#tanzania">Women Empowering Women Entrepreneurs</a>, dat gesteund wordt door VZW GreenForward en <a href="https://www.facebook.com/weedo.womenempowering/">WEEDO (Women Empowering and Entrepreneurship Development Organization)</a>.
