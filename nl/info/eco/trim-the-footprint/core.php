                <div class="tab-content rounded p-0 p-md-2" id="TrimTheFootprint-tab">
                    <div class="tab-pane fade show active" id="conscious-communication" role="tabpanel" aria-labelledby="tab-1">
                        <h4 class="text-irie"><b>Axe 1</b></h4>
                        <p>
                            Door een systematische communicatie via website, nieuwsbrief en social media willen we het project
                            verder verbreden en het ook buiten de doelgroep van elke afzonderlijke actie laten groeien. Via sociale media willen we de interactiviteit van het publiek aanmoedigen. Door het gebruik van een gezamenlijke slogan en logo willen we het project visueel herkenbaar maken. Door het uitnodigen van organisatoren van andere evenementen hopen we bepaalde acties verder te laten verspreiden en door het betrekken van de onderwijswereld willen we resultaten op lange termijn installeren.
                        </p>
                    </div>
                    <div class="tab-pane fade" id="trim-the-footprint" role="tabpanel" aria-labelledby="tab-2">
                        <h4 class="text-irie"><b>Axe 2</b></h4>
                        <p>
                            We willen voor elke partner een carbon footprint berekening laten maken op basis van beschikbare gegevens van het event in 2019, volgens de CO2 prestatieladder. Daarna willen wij per partner, maar ook evenement-overschrijdend in overleg gaan welke mogelijke maatregelen met betrekking tot energiegebruik, afval, mobiliteit, voedsel, water er mogelijk zijn. Hierbij proberen we rekening te houden met kostprijs, communicatie, betrouwbaarheid en duurzaamheid. Op die manier verkleinen we niet alleen onze eigen voetafdruk, maar willen we ook een voorbeeld zijn naar andere evenementen toe.
                        </p>
                    </div>
                    <div class="tab-pane fade" id="changing-minds" role="tabpanel" aria-labelledby="tab-3">
                        <h4 class="text-irie"><b>Axe 3</b></h4>
                        <p>
                            Festivalgangers worden aangesproken op een uitdagende manier : de muziek trekt hen aan, maar er wordt ruimte gecreëerd om ook iets anders te ontdekken. Zo hopen we mensen aan te zetten tot concrete verandering in hun dagelijks leven. Doordat we ecologische organisaties de ruimte en mogelijkheid aanbieden hun werking aan een breder publiek voor te stellen, stimuleren we hen tot het verder ontwikkelen van hun werking. Door het uitnodigen van andere evenementorganisatoren en lokale overheden, hopen we een ecologisch vuurtje te verspreiden dat verder kan branden. We verlagen de drempel en brengen overheden, evenement- en eco-organisaties en festivalpubliek bij elkaar om zo langetermijns effecten te creëren.
                        </p>
                    </div>
                    <div class="tab-pane fade" id="food-waste-facts" role="tabpanel" aria-labelledby="tab-4">
                        <h4 class="text-irie"><b>Axe 4</b></h4>
                        <p>
                            We willen samen een tweetalige bundel creëren waarin op het niveau van 10-12-jarigen meer uitleg
                            gegeven wordt over Food Waste. In deze bundel willen we een vijftal recepten naar voren schuiven (koken met restjes, koken met afval). We leggen hiervoor contact met diverse chefkoks. De bundel zal zowel digitaal als in een geprinte versie beschikbaar zijn. We houden rekening met creatieve en inspirerende methodieken in het opstellen van de bundel. Met deze bundel willen we naar de diverse scholen toe stappen. Hij wordt gratis aangeboden. Indien gewenst, kan iemand uit de drie partner teams de bundel aan de hand van een workshop voorstellen in de scholen. Door de brug te slaan naar het onderwijs willen wij onze evenementen linken aan het lokale leven én willen wij het project verankeren naar de toekomst toe.
                        </p>
