                            <a id="home"></a>
                            <p><b>I = I&I</b> -- Herdenk je stappen vooraleer je een anders lastig valt.</p>
                            <ol>
                                <li>Als uw herder der schapen willen we uw veiligheid garanderen. Volg het vuur in je hart, maar stook er geen extra. Ter alle veiligheid, des is vuur tussen alle tensen verboden</li>
                                <li>Respecteer uw Moeder en haar bezittingen. Wat je niet in je moeke's zetel gooit, zal je ook niet op Moeder Natuur's zetel gooien</li>
                                <li>Stelen zal je niet tenzij het een hart zal wezen. Laat geen waardevolle spullen in je tent, zodat dieven geen cent verdienen, uitgesproken hel op 't eind eind eind(<i>dubwise</i>)</li>   
                                <li>Herrinner je de Sabbat. Men wilt camperen omdat men wilt rusten. Plaats om te roepen zal op het festival wezen</li>
                                <li>Voedsel en drank is toegelaten op de camping</li>
                                <li>Voor dit paradijs bedaal je maar een prijs, zo kan douchen en plassen uit eigen wijs</li>
                                <li>Je zal niet stelen of anderman's tent betreden. Inclusief de geit</li>
                            </ol>
                            <hr/><br/>
                            <a id="camping_location"></a>
                            <p class="text-white"><b>--> CAMPING LOCATIE</b></p>
                            <p>De IRIE Camping is te vinden aan de <b>Nachtegaalstraat 1</b>.</p>
                            Irie Vibes heet de wereld beide welkom op het festival en de camping! Een kampeerplek kost <b>€10</b>, verkrijgbaar door de aankoop van een polsbandje aan de ingang van de camping.<br/><br/>
                            <b>Inbegrepen in de prijs:</b><br/>
                            <ul>
                                <li>Toilet- en douchegebruik</li>
                                <li>Vuilniszakken en een asbak</li>
                                <li>Een goede omgeving</li>
                                <li>Een vriendelijke lach van onze medewerkers</li>
                            </ul>
                            De camping opent op donderdag om 12u en gaat dicht op zondag om 11u. We verwachten tegen dit uur dat alle bezoekers de camping verlaten en een propere plaats achter laten.<br/><br/>                             
                            <b>De IRIE Campîng is open voor het publiek op:</b><br/>
                            <ul>
                                <li>Donderdag 26 juli, vanaf 18u</li>
                                <li>Vrijdag 27 juli, vanaf 17u</li>
                                <li>Zaterdag 28 juli, vanaf 12u</li>  
                            </ul>
                            <i>De camping is tijdens het festival 24u/24u open voor plaatshouders.</i><br/>
                            <hr/>
                            <a id="agreements"></a>
                            <p class="text-white"><b>--> Overeenkomsten</b></p>
                            <p>Het festival en de camping zijn in een rustige omgeving gesitueerd.</p>
                            <b>We vragen je om:</b>
                            <ul> 
                                <li>Geen schade aan te brengen aan de omgeving of structuren</li>
                                <li>Respect te tonen aan alle levende wezens in en rondom de camping, inclusief nabije velden</li>  
                                <li>De grond waar kinderen lopen glas- en afvalvrij te houden. Breng uw afval naar de vuilnisbak</li>
                            </ul>
                            <p class="text-danger"><i>Elk vuur is verboden als een veiligheidsmaatregel.</i></p>
                            <p>We vragen U vriendelijk om naar de camping te komen met goeie bedoelingen:<br/> 
                                <b>Wapens of andere gevaarlijke objecten horen niet thuis op de camping.</b>
                            </p> 
                            <p>Elke vorm van racisme, oneerlijkheid, vechgedrag of vandalisme wordt niet getolereerd.</p>
                            <p>Hou altijd rekening met andere gasten, en let op uw geluidsniveau.</p>
                            <p>We houden van dieren, maar we kunnen huisdieren niet toelaten op de camping.
                                <b>Dieren die gevonden worden zullen in een opvangplaats terecht gebracht worden en kunnen nadien opgepikt worden door hun baasje.</b>Er wordt natuurlijk een uitzondering gemaakt voor blind-geleide honden.
                            </p>
                            <hr/>
                            <a id="facilities"></a>
                            <p class="text-white"><b>--> Faciliteiten</b></p>
                            <p>Toiletten en douches kunnen worden gebruikt op de camping.
                                Kraanwater kan worden gebruikt, maar het is <u>geen drinkwater.</u></p>
                            De douches zijn open op:
                            <ul>
                                <li>Vrijdag: van 9u tot 12u en van 17u tot 19u</li>
                                <li>Zaterdag: van 9u tot 12u en van 17u tot 19u</li>
                                <li>Zondag: van 9u tot 12u (uiteindelijke sluitingsuur)</li>
                            </ul>
                            <p><i>Voor elk probleem of elke vraag staan onze medewerkers steeds voor je klaar</i></p>
                            <hr/>
                            <a id="food_drinks"></a>
                            <p class="text-white"><b>--> Drank & Voedsel</b></p>
                            <p>Eigen voedsel of drank is toegelaten op de camping, maar <b>niet</b> op het festival.</p>
                            <p>Elke campeerder die een volle (eerlijke) vuilniszak kan terugbezorgen krijgt een gratis kop koffie.</p>
                            <p><i>Ontbijt en brood kan worden verkregen op het campeerterrein.</i></p>
                            <hr/>
                            <a id="entrance_exit"></a>
                            <p class="text-white"><b>--> Ingang & Uitgang</b></p>
                            <p>Het festivalterrein en de camping zijn toegankelijk via de ingang op de <u>Koekelarestraat</u>. De parking is toegankelijk via de ingang in de  <u>Vinckaertstraat</u>.Borden zullen je de weg wijzen. Kampeerwagens parkeren op een apart stuk van de parking.</p>
                            <p>Als je op de camping verblijft, dan <i>parkeer je eerst op de parking vooraleer uit te pakken.</i>. Er is <b>geen</b> zone bij de camping om je spullen achter te laten vooraleer je parkeert. Dit jaar is er opnieuw eenrichtingsverkeer.</p>
                            <p>Een kampeervergunning kun je aanschaffen aan de ingang van het festival, waar je ook je ticket koopt of cumuleert. Campingmedewerkers zullen U naar uw kampeerplek begeleiden.</p>
                            <p class="text-danger"><i>De nooduitgangen moeten ten alle tijden vrij blijven. Verkeerd geplaatste auto's of tenten zullen worden verwijderd.</i></p>
                            <hr/>
                            <a id="camping_crimes"></a>
                            <p class="text-white"><b>--> Camping Crimes</b></p>
                            <p>Om een aangename omgeving te verzorgen moeten we ons aan een aantal regels voordoen:</p> 
                            <p><i>Mensen die de regels niet respecteren of schade toedienen zullen verwijderd worden en kunnen gevraagd worden om een compensatie.</i></p> 
                            <p><b>Bij zware overtredingen dient de politie ingelicht te worden</b>. Tickets worden in geen van bijde gevallen terugbetaald.</p>
                            <hr/>
                            <a id="personal_belongings"></a>
                            <p class="text-white"><b>--> Persoonlijke Spullen</b></p>
                            Breng nooit erg dure bezittingen mee naar het festival en laat geen dure voorwerpen onbewaakt achter in je tent. (<i>Nee, ook niet je vriend/vriendin...</i>)
                            <hr/>
                            <p class="highlight-black">IRIE VIBES ROOTS FESTIVAL kan niet verantwoordelijk worden gehouden voor eventuele ongevallen of diefstal.</p>
