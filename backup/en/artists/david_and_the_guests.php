                    <h2 class="display-artist text-center">David and The Guests</h2>
                    <p>In his student years and following, David (Decaesstecker) played as guitarist in a variety of rock bands. One of those bands was the Kortemark rockband ‘Golliwog’ where he took the role as lead singer. Aside from Golliwog’s presence in playing cover songs, they also played David’s own tracks. After years of fun and many well played concerts, the group sadly split up.  David, however, didn’t sit still. The vibes tickled again as he decided to go live again in 2016 as singer-songwriter in the Ghent dialect. Knowing David has a wide selection and taste in music, sticking to one genre wasn’t enough. Aside from Singer-song, rock and blues have also made their way in.</p>
                    <p>A special for Kortemark Congé has been arranged on Thursday July 26th, where David collected his old drummer Vinie and basist Steven from Golliwog back together. Furthermore there’s harmonica by Thomas, and Nico playing the Hammond. Together, they’re ready to play some amazing tracks  from both David & Golliwog, but also covers of Claptop, Springsteen and The Stones.</p>
