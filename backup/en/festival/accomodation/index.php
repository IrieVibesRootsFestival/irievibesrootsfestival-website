                                <p><b>I = I&I</b> -- Reconsider your doings before you annoy other beings.</p>
                                <ol>
                                    <li>As loving shepherds we want you to be safe so only the fire in your heart can burn. Between the tents every flame is forbidden,  so safety we earn</li>
                                    <li>Honor thy mother and respect her.  What you don't throw in your momma's living room; you don't throw on Mother Nature’s ground</li>
                                    <li>Thou shalt not steal unless it's a heart, thou shalt not leave values in thy tent; so for thieves no reward, only hell in the end end end end end (<i>dubwise</i>)</li>   
                                    <li>Remember the sabbath day , the campsite is for people to rest thou can reason at low level here but keep thy roar for the fest</li>
                                    <li>Food & drinks can be brought to the camp</li>
                                    <li>For this paradise, thou shalt pay only one fee, so you’re at ease for a shower or a pee</li>
                                    <li>Thou shalt not covet your neighbor's house, his ox or donkey, or anything that belongs to thy neighbor</li>
                                </ol>
                                <hr/><br/>
                                <a id="camping_location"></a>
                                <p class="text-white"><b>--> CAMPING LOCATION</b></p>
                                <p>The IRIE camping is situated at <b>Nachtegaalstraat 1</b>.</p>
                                Irie Vibes welcomes the world and indeed also at the camping! A spot on the camping ground costs <b>€10</b>, available to obtain through purchasing a wristband at the entrance of the camping. <br/><br/>
                                <b>This price includes:</b><br/>
                                <ul>
                                    <li>Use of toilets and showers</li>
                                    <li>Garbage bags and an ashtray</li>
                                    <li>A great atmosphere</li>
                                    <li>A friendly smile from our camping volunteers</li>
                                </ul>
                                The camping opens on Thursday at 12PM and closes on Sunday at 11AM. We expect each visitor to leave the camping site and leave a clean spot by that time.<br/><br/>                             
                                <b>The Irie Vibes Roots Festival is open to the public on: </b><br/>
                                <ul>
                                    <li>Thursday July 26th, from 6PM</li>
                                    <li>Friday July 27th, from 5PM</li>
                                    <li>Saturday July 28th, from 12PM</li>  
                                </ul>
                                <i>The camping is open 24h/24h during this whole weekend.</i><br/>
                                <hr/>
                                <a id="agreements"></a>
                                <p class="text-white"><b>--> AGREEMENTS</b></p>
                                <p>The festival and camping are situated in a quiet environment.</p>
                                <b>We ask you to:</b>
                                <ul> 
                                    <li>Not cause any environmental damage to fields or properties</li>
                                    <li>Show respect to all living creatures, including the animals in the fields nearby the camping</li>  
                                    <li>Keep the ground where children are walking on safe. Bring your cans, glass or other garbage to the trash bins.</li>
                                </ul>
                                <p class="text-danger"><i>All kinds of fire are forbidden as a safety measure.</i></p>
                                <p>We kindly ask you to come to the camping with the right intentions:<br/> 
                                    <b>Weapons or any dangerous objects do not belong at the camping or the festival.</b>
                                </p> 
                                <p>All forms of fighting, injustice, racism or vandalism are disrespectful and not allowed.</p>
                                <p>Always consider the other guests and keep the noise down. New this year is the <i>Silent Camping</i>. 
                                    More information can obtained with the camping volunteers.
                                </p>
                                <p>Although we love animals, we cannot admit them to the festival or camping.
                                    <b>Animals that are found will be taken to the shelter where the owner can pick it up later.</b> 
                                    An exception is made for guide dogs.
                                </p>
                                <hr/>
                                <a id="facilities"></a>
                                <p class="text-white"><b>--> Facilities</b></p>
                                <p>Toilets and showers can be used at the camping site.
                                    Tap water can be used, although the water is <u>not safe for drinking.</u></p>
                                Showers are open on:
                                <ul>
                                    <li>Friday from 9AM till 12 o'clock and from 5PM ‘till 7PM</li>
                                    <li>Saturday from 9AM till 12 o'clock and from 5PM ‘till 7PM</li>
                                    <li>Sunday from 9AM till 12 o'clock and from 5PM ‘till 7PM</li>
                                </ul>
                                <p><i>For all kind of questions or problems, the Camping Team will be there for you.</i></p>
                                <hr/>
                                <a id="food_drinks"></a>
                                <p class="text-white"><b>--> Food & Drinks</b></p>
                                <p>You can bring your own food or drinks to the camping, but not to the festival.</p>
                                <p>Each camping member that brings a fully loaded trash bag to the entrance, is offered a free coffee. ¨<b>Please do not abuse this reward.</b></p>
                                <p><i>Breakfast and toasts can be purchased at the catering tent on the camping ground.</i></p>
                                <hr/>
                                <a id="entrance_exit"></a>
                                <p class="text-white"><b>--> Entrance & Exit</b></p>
                                <p>The Nachtegaalstreet is <b>one-way traffic</b> during the festival. If you come by car, keep in mind that there’s only space to load or pack your stuff in the <i>Kiss&Ride zone</i>. When you’re ready, you will be asked to <u>park your vehicle on the provided parking.</u> <b>Parking in the street is prohibited.</b></p>
                                <p>Use the marked entrance and exit. Camping volunteers will show you where to install the tents. </p>
                                <p class="text-danger"><i>The emergency exits need to be accessible at any time. Wrongly placed tents or cars will be removed.</i></p>
                                <hr/>
                                <a id="camping_crimes"></a>
                                <p class="text-white"><b>--> Camping Crimes</b></p>
                                <p>To provide a nice and safe festival for all, we need to set some basic rules and agreements:</p> 
                                <p><i>People who do not respect them or cause damage can be removed from the territory and/or asked to pay a compensation.</i></p> 
                                <p><b>Serious violations of the rules will require the local police department to be informed</b>. Tickets will not be refunded in either cases.</p>
                                <hr/>
                                <a id="personal_belongings"></a>
                                <p class="text-white"><b>--> Personal Belongings</b></p>
                                Never bring expensive personal belongings to the festival ground and never leave precious things unguarded in your tent (<i>not even your boyfriend/girlfriend</i>.)
                                <hr/>
                                <p class="highlight-black">IRIE VIBES ROOTS FESTIVAL cannot be held responsible for any accidents, crimes or theft.</p>
