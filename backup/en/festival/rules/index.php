                <p class="text-white">--> FACILITIES</p>
                <ul>
                    <li><b>CSP (Central Security Point)</b>: ready to help you out with all kinds of questions about safety at Irie Vibes.  You can also get free earplugs, sunscreen for the children, Irie Vibes tshirts, gadgets and pocket ash trays.</li>
                    <li><b>Toilets</b>:  €1 to pee all weekend! It is not allowed to pee outside the toilet area!</li>
                    <li><b>Ecoteam</b>: Be nice to our eco-team and mother nature! Please throw your garbage in the garbage bins!</li>
