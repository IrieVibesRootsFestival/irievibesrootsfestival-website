            <h1 style="margin-bottom:5px" class="display-7 text-center">Become Volunteer</h1>
            <br/>
            <div class="highlight-black">The volunteer jobs for the <span class="text-danger">build-up</span> have all been taken.</div>
            <br/>
            <div id="volunteers_form" class="container">
                <p class="test-justify text-center">This year we are again looking for approx. 150 helping hands to turn the Irie Vibes Roots Festival into a success. Volunteers make the difference!</p>
                <p class=" text-center">Each activity will be supervised by a team leader, who will be there during the whole festival if you have  practical questions or problems during your shift.  For all other questions, please contact Cindy Jonckheere, the head of volunteers. </p>
                <br/>
                <hr/>
                <br/>
                <h1 class="display-8">We offer</h1>
                <ul class="list-group">
                    <li class="list-group-item"><b>--> VOLUNTEERS ON THE FESTIVAL</b></li>
                    <div class="list">
                        <p> - You get free access to the festival terrain and an exclusive Irie Vibes T-shirt</p>
                        <p> - Per shift you worked, you will receive 5 drink tickets and a food package (which includes a sandwich and a bowl of soup)</p>
                        <p> - Free use of the parking and camping and all sanitary facilities.</p>
                    </div>
                    <li class="list-group-item"><b>--> VOLUNTEERS BUILDING UP</b></li>
                    <div class="list">
                        <p> - Volunteers who help 3 full days during the construction of the festival receive the same as a volunteer who works 2 shifts on the festival. For every extra full day you worked during the building up, you get 5 drink vouchers extra.</p>
                        <p>- There are soft drinks and (warm) meals.</p>
                        <p> - Volunteers who help during the build-up receive our gratifying love.</p>
                    </div>
                    <li class="list-group-item"><b>--> VOLUNTEERS DISMANTELING</b></li>
                    <div class="list">
                        <p> - Volunteers who help during the dismanteling, will get their ticket refunded when actually showing up to help.</p>
                        <p> - Volunteers who help will also receive a bucket full of appreciation.</p>
                    </div>
                </ul>
                <br/>
                <h1 class="display-8">We ask</h1>
                <div class="list">
                    <p> - At least 3 days of construction or 2 days of dismantling </p>
                    <p> - 2 shifts of 4 hours during the festival </p>
                    <p> - Leaders that are willing to take a position as a team leader (incl. briefing in advance)
                    </p>
                </div>
                <i>Volunteering for more than one shift per day <i>is</i> possible.</i>
                <br/>
                <br/>
                <h1 class="display-8">Agreements</h1>
                <div class="list">
                    <p> - You are obligated to sign in before every new shift. You do this at least 15 minutes before the start of your shift at the main entrance</p>
                    <p> - You work 2 shifts of 4 hours during the festival</p>
                    <p> - You will wear your t-shirt certainly during your shift</p>
                    <p> - You will stay sober during your shift</p>
                </div> 
                <br/>

                <div id="volunteers_alcohol_notice" >
                    <div class="text-center"><p class="highlight-black">Please note:</p></div>
                    <p> - To register for a shift in or with the bar, a minimum age of 18 is required.</p>
                    <p> - Registering as a team leader requires sufficient involvement with the festival in current of past times.</p>
                    <p> - Volunteers with a bar shift have to follow a mandatory course on how to handle dificult or dangerous situations. This course will take only several minutes, and will yield you a certficate.</p>
                </div>
